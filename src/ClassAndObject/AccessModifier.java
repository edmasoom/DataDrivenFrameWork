package ClassAndObject;

public class AccessModifier {

	public static void main(String[] args) {
		
		for(int i=0; i<=3; i++) {
		    for(int j=1;j<=3;j++){  
		    	
		    if(i==2&&j==2) {
		    	break;
		    }System.out.println(i +""+j );
		}
		    
	int d = 1; 
	while(d<=10) {
		
		if(d==7) {
			break; 
		}System.out.println(d);
		d++;
	}
	
	int p = 1; 
	do { 
		if(p==5) {
			break; 
		}		
		System.out.println(p);
		p++;
	} while(p<=10); 
	}
		
			
		
		}
		
		
	}

